using System;

namespace LLT
{
	public struct CoreRect
	{
	    public float X;
	
	    public float Y;
	
	    public float Width;
	
	    public float Height;
	
	    public CoreRect(float x, float y, float width, float height)
	    {
	        X = x;
	        Y = y;
	        Width = width;
	        Height = height;
	    }
	}
}